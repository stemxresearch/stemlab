
def max_rows(nrows: int = 50) -> int:
    """
    Return the maximum number of rows for data-related operations.

    Parameters
    ----------
    nrows : int, optional (default=50)
        The maximum number of rows to be used in data-related 
        operations. Default is 50.

    Returns
    -------
    nrows : int
        The maximum number of rows.
    """
    return nrows


def max_cols(ncols: int = 50) -> int:
    """
    Return the maximum number of columns for data-related operations.

    Parameters
    ----------
    ncols : int, optional (default=50)
        The maximum number of columns to be used in data-related 
        operations.

    Returns
    -------
    ncols : int
        The maximum number of columns.
    """
    return ncols